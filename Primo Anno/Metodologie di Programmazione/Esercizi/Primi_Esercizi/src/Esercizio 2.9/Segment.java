
public class Segment {
    private Point punto1;
    private Point punto2;

    public Segment(Point punto1, Point punto2) {
        this.punto1 = punto1;
        this.punto2 = punto2;
    }

    public Point getPunto1() {
        return this.punto1;
    }

    public Point getPunto2() {
        return this.punto2;
    }
    
}
